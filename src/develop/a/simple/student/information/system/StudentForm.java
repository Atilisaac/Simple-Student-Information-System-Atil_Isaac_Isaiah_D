    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
       try {
        // Check if user typed something in the ID field
        if (!txtId.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Do not fill ID — it will be generated automatically!");
            return;
        }

        // Validate required fields
        if (txtFirstName.getText().trim().isEmpty() ||
            txtLastName.getText().trim().isEmpty() ||
            txtAge.getText().trim().isEmpty() ||
            txtEmail.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "All fields are required!");
            return;
        }

        // Parse age safely
        int age;
        try {
            age = Integer.parseInt(txtAge.getText().trim());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Age must be a number!");
            return;
        }

        // Create new student object (do not set ID)
        Student student = new Student();
        student.setFirstName(txtFirstName.getText().trim());
        student.setLastName(txtLastName.getText().trim());
        student.setAge(age);
        student.setEmail(txtEmail.getText().trim());

        // Add to database
        StudentDAO dao = new StudentDAO();
        dao.addStudent(student); // database will assign ID automatically

        JOptionPane.showMessageDialog(this, "Student Added Successfully!");

        // Clear text fields
        txtId.setText("");
        txtFirstName.setText("");
        txtLastName.setText("");
        txtAge.setText("");
        txtEmail.setText("");

    } catch (Exception ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error adding student: " + ex.getMessage());
    }
    }//GEN-LAST:event_btnAddActionPerformed


    private void loadAllStudentsToTable() {
    try {
        StudentDAO dao = new StudentDAO();
        java.util.List<Student> students = dao.getAllStudents();

        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblStudents.getModel();
        model.setRowCount(0); // Clear existing rows

        for (Student student : students) {
            model.addRow(new Object[]{
                student.getStudentId(),
                student.getFirstName(),
                student.getLastName(),
                student.getAge(),
                student.getEmail()
            });
        }
    } catch (Exception ex) {
        ex.printStackTrace();
        javax.swing.JOptionPane.showMessageDialog(this, "Error loading students: " + ex.getMessage());
    }
}


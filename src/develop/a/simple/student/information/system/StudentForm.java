    private void btnShowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnShowActionPerformed
         if (txtId.getText().trim().isEmpty()) {
        // If no ID entered, show all students
        loadAllStudentsToTable();
        return;
    }

    try {
        int id = Integer.parseInt(txtId.getText().trim());
        StudentDAO dao = new StudentDAO();
        Student student = dao.getStudentById(id);

        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblStudents.getModel();
        model.setRowCount(0); // Clear table

        if (student != null) {
            model.addRow(new Object[]{
                student.getStudentId(),
                student.getFirstName(),
                student.getLastName(),
                student.getAge(),
                student.getEmail()
            });
        } else {
            JOptionPane.showMessageDialog(this, "Student not found!");
        }
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "ID must be a number!");
    }
    }//GEN-LAST:event_btnShowActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
        // Ensure an ID is entered
        if (txtId.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter Student ID!");
            return;
        }

        int id = Integer.parseInt(txtId.getText().trim());
        StudentDAO dao = new StudentDAO();
        Student student = dao.getStudentById(id);

        // If student not found
        if (student == null) {
            JOptionPane.showMessageDialog(this, "Student ID not found!");
            return;
        }

        // Show student info and ask for confirmation
        String message = "Are you sure you want to delete this student?\n\n" +
                         "ID: " + student.getStudentId() + "\n" +
                         "First Name: " + student.getFirstName() + "\n" +
                         "Last Name: " + student.getLastName() + "\n" +
                         "Age: " + student.getAge() + "\n" +
                         "Email: " + student.getEmail();

        int confirm = JOptionPane.showConfirmDialog(this, message, "Confirm Delete", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            dao.deleteStudent(id); // delete from database

            // Remove from JTable if present
            javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblStudents.getModel();
            for (int i = 0; i < model.getRowCount(); i++) {
                Object value = model.getValueAt(i, 0);
                if (value != null && Integer.parseInt(value.toString()) == id) {
                    model.removeRow(i);
                    break;
                }
            }

            JOptionPane.showMessageDialog(this, "Student deleted successfully!");
            
            // Clear text fields
            txtId.setText("");
            txtFirstName.setText("");
            txtLastName.setText("");
            txtAge.setText("");
            txtEmail.setText("");
        }

    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "ID must be a number!");
    } catch (Exception ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error deleting student: " + ex.getMessage());
    }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
        // Ensure an ID is entered
        if (txtId.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter Student ID!");
            return;
        }

        int id = Integer.parseInt(txtId.getText().trim());
        StudentDAO dao = new StudentDAO();

        // Step 1: If text fields are empty → fetch student info
        if (txtFirstName.getText().trim().isEmpty() &&
            txtLastName.getText().trim().isEmpty() &&
            txtAge.getText().trim().isEmpty() &&
            txtEmail.getText().trim().isEmpty()) {

            Student student = dao.getStudentById(id);
            if (student != null) {
                // Fill text fields for editing
                txtFirstName.setText(student.getFirstName());
                txtLastName.setText(student.getLastName());
                txtAge.setText(String.valueOf(student.getAge()));
                txtEmail.setText(student.getEmail());

                JOptionPane.showMessageDialog(this, "Student info loaded. Edit fields and click Update again!");
            } else {
                JOptionPane.showMessageDialog(this, "Student ID not found!");
            }
            return; // Stop here, wait for next click
        }

        // Step 2: Save changes from text fields → update database
        int age = Integer.parseInt(txtAge.getText().trim());

        Student student = new Student();
        student.setStudentId(id);
        student.setFirstName(txtFirstName.getText().trim());
        student.setLastName(txtLastName.getText().trim());
        student.setAge(age);
        student.setEmail(txtEmail.getText().trim());

        dao.updateStudent(student); // UPDATE DATABASE

        // Update JTable safely
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblStudents.getModel();
        boolean foundInTable = false;
        for (int i = 0; i < model.getRowCount(); i++) {
            Object value = model.getValueAt(i, 0); // get ID from table
            if (value != null) {
                int tableId;
                if (value instanceof Number) {
                    tableId = ((Number) value).intValue();
                } else {
                    try {
                        tableId = Integer.parseInt(value.toString());
                    } catch (NumberFormatException ex) {
                        continue; // skip invalid rows
                    }
                }

                if (tableId == id) {
                    model.setValueAt(student.getFirstName(), i, 1);
                    model.setValueAt(student.getLastName(), i, 2);
                    model.setValueAt(student.getAge(), i, 3);
                    model.setValueAt(student.getEmail(), i, 4);
                    foundInTable = true;
                    break;
                }
            }
        }

        // If student not found in table → add new row
        if (!foundInTable) {
            model.addRow(new Object[]{id, student.getFirstName(), student.getLastName(), student.getAge(), student.getEmail()});
        }

        JOptionPane.showMessageDialog(this, "Student Updated Successfully!");

    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "ID and Age must be numbers!");
    } catch (Exception ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error updating student: " + ex.getMessage());
    }
    }//GEN-LAST:event_btnUpdateActionPerformed

